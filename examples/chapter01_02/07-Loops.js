console.log("**** for example ****");
// for - example
for (let i = 0; i < 10; i++) {
  console.log(i);
}

console.log("**** while example ****");
let j = 0;
while (j < 10) {
  console.log(j);
  j++;
}

console.log("**** do-while example ****");
let k = 0;
do {
  console.log(k);
  k++;
} while (k < 10);
